//警示視窗
alert('Hello World!');
//變數(不可放數字)
var message = 'Hello World!';

//8.9行意思相同
msg = 'Hello World';
msg += 'foobar';
msg = msg + 'foobar';

//變數相加
var a = 'Hello World!';
var b = 'foobar';
var msg = a+b;

//數值相加
var a = 1;
var b = 2;
var msg = a+b;
alert(msg);

//引號是字串
var a ='1';
var b ='2';
var c ='3';
var msg = a + b + parseInt(c);
alert(msg);